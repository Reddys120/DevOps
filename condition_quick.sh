Numeric Comparison:
Operator	Description
-eq	Equal
-ne	Not equal
-gt	Greater than
-lt	Less than
-ge	Greater or equal
-le	Less or equal

String Comparison:
Operator	Description
=	Equal
!=	Not equal
-z	Empty string
-n	Not empty

File Test:
Operator	Description
-e	File exists
-f	Regular file
-d	Directory
-r	Readable
-w	Writable
-x	Executable