| Data Type         | Description                   | Example                          |
| ----------------- | ----------------------------- | -------------------------------- |
| String            | Default type in Bash          | `name="Alice"`                   |
| Integer           | Used in arithmetic operations | `count=5`, `((count++))`         |
| Array             | Indexed collection            | `arr=(1 2 3)`                    |
| Associative Array | Key-value pairs               | `declare -A map`, `map[key]=val` |
